package ru.guybydefault.comeoutbot.api.object;

public class RequestParam {
    private String key;
    private String value;

    public RequestParam() {
    }

    public String getKey() {
        return key;
    }

    public void setKey(String key) {
        this.key = key;
    }

    public String getValue() {
        return value;
    }

    public void setValue(String value) {
        this.value = value;
    }
}
